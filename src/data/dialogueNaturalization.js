const rewrite = (beatId, text) => {
  let current = text;
  for (let pass = 0; pass < 4; pass += 1) {
    const next = copy.get(`${beatId}|${current}`);
    if (!next || next === current) break;
    current = next;
  }
  return current;
};

export function naturalizeDialogueBeats(beats) {
  return beats.map(beat => ({
    ...beat,
    messages: (beat.messages || []).map(message => {
      if (message.type === "choice") {
        return {
          ...message,
          options: (message.options || []).map(option => ({
            ...option,
            text: rewrite(beat.id, option.text)
          }))
        };
      }

      if (!message.text) return message;
      return { ...message, text: rewrite(beat.id, message.text) };
    })
  }));
}

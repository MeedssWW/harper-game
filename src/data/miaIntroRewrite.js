const humanizeMiaBeat = (beat) => ({
  ...beat,
  messages: (beat.messages || []).map(message => {
    if (message.type === "choice") {
      const replacements = miaChoiceCopy[beat.id];
      if (!replacements) return message;
      return {
        ...message,
        options: message.options.map((entry, index) => ({
          ...entry,
          text: replacements[index] || entry.text
        }))
      };
    }

    if (!message.text || !miaCopy.has(message.text)) return message;
    return { ...message, text: miaCopy.get(message.text) };
  })
});

export const miaIntroRewriteBeats = miaIntroRewriteBase.map(humanizeMiaBeat);

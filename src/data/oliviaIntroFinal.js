const humanizeOliviaBeat = (beat) => ({
  ...beat,
  messages: (beat.messages || []).map(message => {
    if (message.type === "choice") {
      const replacements = oliviaChoiceCopy[beat.id];
      if (!replacements) return message;
      return {
        ...message,
        options: message.options.map((entry, index) => ({
          ...entry,
          text: replacements[index] || entry.text
        }))
      };
    }

    if (!message.text || !oliviaCopy.has(message.text)) return message;
    return { ...message, text: oliviaCopy.get(message.text) };
  })
});


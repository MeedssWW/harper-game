const humanizeDerekBeat = (beat) => ({
  ...beat,
  messages: (beat.messages || []).map(message => {
    if (message.type === "choice") {
      const replacements = derekChoiceCopy[beat.id];
      if (!replacements) return message;
      return {
        ...message,
        options: message.options.map((entry, index) => ({
          ...entry,
          text: replacements[index] || entry.text
        }))
      };
    }

    if (!message.text || !derekCopy.has(message.text)) return message;
    return { ...message, text: derekCopy.get(message.text) };
  })
});

export const derekMorningRewriteBeats = derekMorningRewriteBase.map(humanizeDerekBeat);
